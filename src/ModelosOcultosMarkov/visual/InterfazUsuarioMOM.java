    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String textoActividad = jTextField2.getText();
        String textoClima = jTextField3.getText();

        if (!textoActividad.isEmpty() && !textoClima.isEmpty()) {
            double resultadoActividad = mom.probabilidadDeHacerActividad(textoClima, textoActividad);
            if (resultadoActividad < 0){
                JOptionPane.showMessageDialog(null, "Ingrese un estado valido");
            } else {
                resultadoActividad = resultadoActividad * 100;
                jLabel9.setText("Resultado: " + resultadoActividad + "%");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese los datos correctamente");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

